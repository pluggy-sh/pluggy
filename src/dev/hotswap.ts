/**
 * HotswapAgent integration for `pluggy dev`.
 *
 * Three responsibilities:
 *   1. Provision the HotswapAgent JAR into the user cache.
 *   2. Render `hotswap-agent.properties` so HA watches the build's exploded
 *      classes directory (`.pluggy-build/<hash>/`) and redefines classes in
 *      place when javac rewrites them.
 *   3. Expose a category-shaped watcher (`hotswap.start`) that taps the
 *      server's stdout for HA's success/failure markers and surfaces the
 *      outcome to the dev loop's fallback chain.
 *
 * The agent JAR alone allows method-body changes; pair with a JBR `java`
 * (see `./jbr.ts`) and `-XX:+AllowEnhancedClassRedefinition` for the full
 * DCEVM-grade redefinition (new methods, fields, supertype changes).
 */

import type { ChildProcess } from "node:child_process";
import { existsSync } from "node:fs";
import { mkdir, rename, writeFile } from "node:fs/promises";
import { join } from "node:path";

import { log } from "../logging.ts";
import { toPosixPath } from "../portable.ts";
import { getCachePath } from "../project.ts";

/**
 * Pinned HotswapAgent release. Bump deliberately — agent log markers and
 * config keys can shift between majors and `wait()` parses them.
 */
export const HOTSWAP_AGENT_VERSION = "2.0.3";

const AGENT_RELEASE_URL = (version: string): string =>
  `https://github.com/HotswapProjects/HotswapAgent/releases/download/RELEASE-${version}/hotswap-agent-${version}.jar`;

/** Cached path to the HotswapAgent JAR (download on miss). */
export async function ensureAgent(version = HOTSWAP_AGENT_VERSION): Promise<string> {
  const cacheDir = join(getCachePath(), "agents");
  const dest = join(cacheDir, `hotswap-agent-${version}.jar`);
  if (existsSync(dest)) return dest;

  await mkdir(cacheDir, { recursive: true });
  const url = AGENT_RELEASE_URL(version);
  log.info(`hotswap: downloading HotswapAgent ${version}…`);
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`hotswap: agent download failed (${res.status} ${res.statusText}) — ${url}`);
  }
  const tmp = `${dest}.partial`;
  await writeFile(tmp, new Uint8Array(await res.arrayBuffer()));
  await rename(tmp, dest);
  log.debug(`hotswap: cached agent at ${dest}`);
  return dest;
}

export interface PropertiesFileOptions {
  /** Absolute path to the build's exploded classes directory. */
  classesDir: string;
  /** `info` is the default; `debug` for verbose markers in playground testing. */
  logLevel?: "error" | "warning" | "info" | "debug" | "trace";
}

/**
 * Render a `hotswap-agent.properties` file.
 *
 * HotswapAgent reads this from each classloader's resources and applies the
 * config to *that* classloader only. We bundle it into the user's plugin JAR
 * (via `BuildOptions.extraStagingFiles`) so HA only attaches the staging
 * directory to the plugin's own classloader. Putting the file on the boot
 * classpath instead would leak `extraClasspath` into the system classloader
 * and Bukkit's `JavaPlugin requires to be created by a valid classloader`
 * guard would fail (parent-first delegation finds the staging-dir copy).
 */
export function renderPropertiesFile(opts: PropertiesFileOptions): string {
  const level = opts.logLevel ?? "info";
  // Java properties files use `\` as an escape, so a Windows path like
  // `C:\Users\…\.pluggy-build\abc` would parse as `C:Users….pluggy-buildabc`.
  // Forward slashes work on every OS — Java's `File` accepts them on Windows.
  const classes = toPosixPath(opts.classesDir);
  return [
    "# Generated by pluggy — do not edit by hand.",
    "# Regenerated on every `pluggy dev` start.",
    `LOGGER=${level}`,
    `extraClasspath=${classes}`,
    "autoHotswap=true",
    // HotswapAgent ships dozens of framework integrations (Spring, Hibernate,
    // JEE, ...). For Bukkit/Paper plugins they're all noise that throws
    // `InvocationTargetException` while trying to instrument unrelated
    // classes. Disable everything except the core class-redefinition path.
    `disabledPlugins=${DISABLED_HA_PLUGINS.join(",")}`,
    "",
  ].join("\n");
}


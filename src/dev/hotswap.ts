/**
 * Build the JVM argument prefix that turns a vanilla `java` invocation into a
 * HotswapAgent-attached one. Returns args in the order they should appear.
 */
export function agentJvmArgs(input: { agentJarPath: string }): string[] {
  return [
    `-javaagent:${input.agentJarPath}`,
    "-XX:+AllowEnhancedClassRedefinition",
    // HotswapAgent uses internal reflection on URLClassLoader/URLClassPath
    // to splice the build's classes dir into the plugin's own classloader.
    // The module system slammed the door on that in Java 17 — these
    // `--add-opens` flags re-open exactly the surface HA pokes at, no more.
    "--add-opens=java.base/java.lang=ALL-UNNAMED",
    "--add-opens=java.base/jdk.internal.loader=ALL-UNNAMED",
    "--add-opens=java.base/java.net=ALL-UNNAMED",
  ];
}

/**
 * Outcome of a single hotswap attempt. The dev loop chooses its fallback
 * strategy based on which value comes back.
 */
export type HotswapOutcome = "reloaded" | "failed" | "timeout";

export interface StartHotswapOptions {
  child: ChildProcess;
  /** Per-attempt timeout. Default 4000ms — generous to absorb slow disks. */
  timeoutMs?: number;
}

/**
 * Result of `hotswap.start(child)` — single-call backbone for the dev loop.
 *
 * Two-step contract: call `arm()` *before* triggering the rebuild, then
 * `await wait()` after the rebuild completes. Splitting them closes a race —
 * HA's filesystem watcher can emit `RELOAD` while the dev loop is still
 * inside `buildProject`, so any data arriving in that window must be
 * retained for the upcoming `wait()` call instead of dropped.
 *
 * `arm()` clears any buffered data from the previous attempt and starts
 * retaining incoming chunks. `wait()` resolves with the first match in the
 * buffer (or the first one to arrive after subscribing), or `"timeout"`
 * if neither happens before `timeoutMs`. Each arm/wait pair is independent.
 *
 * `stop()` detaches all listeners and is idempotent.
 */
export interface HotswapWatcher {
  arm(): void;
  wait(): Promise<HotswapOutcome>;
  stop(): void;
}

// HA's success marker is the `RELOAD` log level fired right before it calls
// `Instrumentation.redefineClasses()`. The redefinition is synchronous and
// silent on success — failures show up as exceptions on the same thread.
const SUCCESS_RE = /HOTSWAP\s+AGENT.*\bRELOAD\b.*Reloading\s+classes/i;
// Narrow to JVMTI-side rejections of an actual redefinition. HA itself emits
// ERROR-level logs for unrelated framework-plugin transformer issues
// (Log4j2, Jackson, JdkPlugin, …) that aren't hotswap failures — matching
// `HOTSWAP AGENT.*ERROR` here would false-positive on those.
const FAILURE_RE =
  /class\s+redefinition\s+failed|cannot\s+redefine|UnsupportedOperationException.*redefin/i;

/**
 * Attach a stdout/stderr line tap to the running JVM and surface HotswapAgent
 * markers as a single `wait()`-able outcome. Stays out of the way otherwise:
 * the existing pipe to `process.stdout` is preserved so the user still sees
 * the server log unchanged.
 */
export function start(opts: StartHotswapOptions): HotswapWatcher {
  const { child } = opts;
  const timeoutMs = opts.timeoutMs ?? 4000;

  const buffers = { stdout: "", stderr: "" };
  // Match this attempt's outcome — sticks once set so `wait()` can pick it up
  // even if the marker arrived between `arm()` and `wait()`.
  let bufferedOutcome: HotswapOutcome | undefined;
  let armed = false;

  let pendingResolve: ((outcome: HotswapOutcome) => void) | undefined;
  let pendingTimer: NodeJS.Timeout | undefined;

  const tryMatch = (line: string): HotswapOutcome | undefined => {
    if (SUCCESS_RE.test(line)) return "reloaded";
    if (FAILURE_RE.test(line)) return "failed";
    return undefined;
  };

  const finishWait = (outcome: HotswapOutcome): void => {
    if (pendingTimer !== undefined) {
      clearTimeout(pendingTimer);
      pendingTimer = undefined;
    }
    const r = pendingResolve;
    pendingResolve = undefined;
    armed = false;
    bufferedOutcome = undefined;
    if (r !== undefined) r(outcome);
  };

  const onChunk =
    (which: "stdout" | "stderr") =>
    (chunk: Buffer | string): void => {
      if (!armed) return;
      buffers[which] += chunk.toString();
      let nl = buffers[which].indexOf("\n");
      while (nl !== -1) {
        const line = buffers[which].slice(0, nl);
        buffers[which] = buffers[which].slice(nl + 1);
        const outcome = tryMatch(line);
        if (outcome !== undefined) {
          // If `wait()` is already pending, deliver immediately. Otherwise
          // remember the outcome for the next `wait()` call — closes the
          // race where HA fires `RELOAD` before the dev loop subscribes.
          if (pendingResolve !== undefined) {
            finishWait(outcome);
          } else {
            bufferedOutcome = outcome;
          }
          return;
        }
        nl = buffers[which].indexOf("\n");
      }
    };

  const onStdout = onChunk("stdout");
  const onStderr = onChunk("stderr");

  child.stdout?.on("data", onStdout);
  child.stderr?.on("data", onStderr);

  let stopped = false;

  return {
    arm(): void {
      if (stopped) return;
      buffers.stdout = "";
      buffers.stderr = "";
      bufferedOutcome = undefined;
      armed = true;
    },
    wait(): Promise<HotswapOutcome> {
      if (stopped) return Promise.resolve("timeout");
      // Caller forgot to arm — be lenient and arm now (matches no buffered
      // marker, just waits fresh). Tests rely on this.
      if (!armed) {
        buffers.stdout = "";
        buffers.stderr = "";
        bufferedOutcome = undefined;
        armed = true;
      }
      // A marker that arrived between arm() and wait() resolves immediately.
      if (bufferedOutcome !== undefined) {
        const o = bufferedOutcome;
        bufferedOutcome = undefined;
        armed = false;
        return Promise.resolve(o);
      }
      return new Promise<HotswapOutcome>((resolvePromise) => {
        pendingResolve = resolvePromise;
        pendingTimer = setTimeout(() => finishWait("timeout"), timeoutMs);
        pendingTimer.unref?.();
      });
    },
    stop(): void {
      if (stopped) return;
      stopped = true;
      child.stdout?.removeListener("data", onStdout);
      child.stderr?.removeListener("data", onStderr);
      if (pendingTimer !== undefined) {
        clearTimeout(pendingTimer);
        pendingTimer = undefined;
      }
      if (pendingResolve !== undefined) {
        const r = pendingResolve;
        pendingResolve = undefined;
        r("timeout");
      }
    },
  };
}
